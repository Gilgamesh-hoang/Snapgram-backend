package org.snapgram.enums;

public enum NotificationType {
    LIKE_POST,          // User A liked User B's post
    COMMENT_POST,       // User A commented on User B's post
    REPLY_COMMENT,      // User A replied to User B's comment
    LIKE_COMMENT,       // User A liked User B's comment
    FOLLOW_USER,        // User A just followed User B
}
